using UnityEngine;

public static class ColorExtension
{
    public static Color PastelRed
    {
        get
        {
            return new Color(0.8823f, 0.3568f, 0.4509f);
        }
    }
    public static Color PastelGreen
    {
        get
        {
            return new Color(0.4572f, 0.8823f, 0.3568f);
        }
    }
}